    public static String totalsaldo(String id, int saldo){
        Transaksi trs = new Transaksi( null, null, null,  0,  null,  null,  null,  null);
        MyDB.move(trs);
        int s = saldo;
        for(Transaksi t: Main.listtransaksi){
            if(t.getIddompet().equals(id)){
                if(t.getKategori().equals("Pengeluaran")){
                    s = s-t.getJumlah();
                }
                else{
                    s = s+t.getJumlah();
                }
            }
        }
        String ss = Integer.toString(s);
        return ss;
    }
    
    public static void tampil(){
        MyDB.move(d);
        DefaultTableModel dtm = (DefaultTableModel) tabelData.getModel();
        dtm.setRowCount(0);
        for(Dompet d: Main.listdompet){
            dtm.addRow(new Object[]{
                d.getNama(),
                d.getJenis(),
                totalsaldo(d.getId(), d.getSaldo()),
            });            
        }
    }
    
        
